    public FrmCustomerViewOrderLines(Customer Cu, int orderid) {
        initComponents();
        
        LoggedUser = Cu;
        OrderID = orderid;
        
        DefaultTableModel orderLines = (DefaultTableModel)tblOrderLines.getModel();
        
        Order order = LoggedUser.getOrders().get(OrderID);
        
        for(Map.Entry<Integer, Orderline> olEntry : order.getOrderLines().entrySet())
        {
            Orderline orderline = olEntry.getValue();
            
            Product orderedproduct = new Product();
            
            String productid = "";
            String productname = "";
            String productprice = "";
            String productquantity = "";
            
            if(orderline.getProduct() == null) // Animal has been deleted
            {
                productid = "N/A";
                productname = "N/A";
                productprice = "N/A";
                productquantity = "N/A";
            }
            else
            {
                orderedproduct = orderline.getProduct();
                productid = String.valueOf(orderedproduct.getProductID());
                productname = orderedproduct.getProductName();
                productprice = "Â£" + String.format("%.02f", orderedproduct.getPrice());
                productquantity = String.valueOf(orderline.getQuantity());
            }
            
            orderLines.addRow(new Object[] {productid,productname,productprice,productquantity}                            );
        }
        
        tblOrderLines.setModel(orderLines);
    }

