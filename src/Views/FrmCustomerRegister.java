    //creates new instance of customer is database from input values
    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        
        String firstname = txtFirstName.getText();
        String lastname = txtLastName.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String housenumber = txtHouseNumber.getText();
        String street = txtStreet.getText();
        String town = txtTown.getText();
        String postcode = txtPostcode.getText();
        
        DBHandler DBH = new DBHandler();
        
        try
        {
            
          if(txtFirstName.getText().equals("") || txtLastName.getText().equals("") || txtUsername.getText().equals("") || txtPassword.getText().equals("") || txtStreet.getText().equals("") || txtTown.getText().equals("") || txtPostcode.getText().equals(""))
          {
              lblError.setText("Error: Please fill all fields");
          }
          else
          {
            
            if(DBH.registerCustomer(new Customer(username,password,firstname,lastname,housenumber ,street,town,postcode)) == true){
                lblError.setText("Register Success");
            }
            else
            {
               lblError.setText("Register Failure");
            }
            
          }
            
        }catch(Exception e)
        {
            lblError.setText("Error: " + e);
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

