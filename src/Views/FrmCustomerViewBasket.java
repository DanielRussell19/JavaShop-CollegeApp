    //initalises privates attributes and GUI components, adds all products from customer order to basket display
    public FrmCustomerViewBasket(Customer Cu) {
        initComponents();
        
        LoggedUser = Cu;
        CustomerOrder = LoggedUser.findLatestOrder();
        
        DefaultTableModel basket = (DefaultTableModel) tblBasket.getModel();
        
        for(Map.Entry<Integer, Orderline> olEntry : CustomerOrder.getOrderLines().entrySet()){
         
            Orderline actualOrderLine = olEntry.getValue();
            
            if(actualOrderLine.getProduct().getClass().getName().equals("Models.Clothing"))
            {
                Clothing tempClothing = (Clothing) actualOrderLine.getProduct();
                basket.addRow(new Object[]{tempClothing.getProductID(),tempClothing.getProductName(),"£" + String.format("%.02f",tempClothing.getPrice()),tempClothing.getStockLevel(),tempClothing.getMeasurement(),0,actualOrderLine.getQuantity()});
            }
            else
            {
                Footwear tempFootwear = (Footwear) actualOrderLine.getProduct();
                basket.addRow(new Object[]{tempFootwear.getProductID(),tempFootwear.getProductName(),"£" + String.format("%.02f",tempFootwear.getPrice()),tempFootwear.getStockLevel(),0,tempFootwear.getSize(),actualOrderLine.getQuantity()});
            }
            
            
        }
        
        tblBasket.setModel(basket);
        lblTotalSum.setText("£" + String.format("%.02f",CustomerOrder.getOrderTotal()));
    }

