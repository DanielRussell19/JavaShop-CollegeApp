    //updates order status to complete in database
    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        if(!CustomerOrder.getOrderLines().isEmpty())
        {
        CustomerOrder.setStatus("Complete");
        
        DBHandler DBH = new DBHandler();
        DBH.completeOrder(CustomerOrder.getOrderID());
        
        FrmCustomerConfirmation newFrm = new FrmCustomerConfirmation(LoggedUser);
        this.dispose();
        newFrm.setVisible(true);   
        }
        else
        {
        this.lblError.setText("Error: Please add a product to basket");
        }
    }//GEN-LAST:event_btnBuyActionPerformed

    //removes product from order basket
    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(tblBasket.getSelectedRow() == -1)
        {
            this.lblError.setText("Error: Select A Product");
        }
        else
        {
            DefaultTableModel basketModel = (DefaultTableModel)tblBasket.getModel();
            int productid = Integer.parseInt(String.valueOf(basketModel.getValueAt(tblBasket.getSelectedRow(), 0))); 
            int quantity = Integer.parseInt(String.valueOf(basketModel.getValueAt(tblBasket.getSelectedRow(), 6)));
            
            //CustomerOrder.changeProductStockLevel(new Product(productid,quantity));
            
            CustomerOrder.removeOrderLine(productid);
            lblError.setText("Product Removed");
            
            DefaultTableModel basket = (DefaultTableModel)tblBasket.getModel();
            basket.removeRow(tblBasket.getSelectedRow());
            
            tblBasket.setModel(basket); 
            
            this.lblTotalSum.setText("£" + String.format("%.02f",CustomerOrder.getOrderTotal()));
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

